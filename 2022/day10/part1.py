@pytest.mark.parametrize(
    ("input_s", "expected"),
    ((INPUT_S, EXPECTED),),
)
def test_case(input_s: str, expected: int) -> None:
    assert solution(input_s) == expected


START_SIGNAL = 20
SIGNAL_STEP = 40
END_SIGNAL = 220


def solution(s: str) -> int:
    x = 1
    cycle_count = 0
    signal = START_SIGNAL
    signal_sum = 0

    for op in s.splitlines():
        prev_x = x
        parts = op.split()
        if parts[0] == "noop":
            cycle_count += 1
        else:
            value = int(parts[1])
            x += value
            cycle_count += 2

        if cycle_count >= signal:
            print(f"cycle={cycle_count} signal={signal * prev_x} ")
            signal_sum += signal * prev_x

            if signal == END_SIGNAL:
                return signal_sum
            signal += SIGNAL_STEP


def main() -> int:
    with open(INPUT_TXT) as f:
        s = f.read()
    print(solution(s))


if __name__ == "__main__":
    main()
